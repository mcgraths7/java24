package com.java24hours;

class PlanetWeight {
    public static void main(String[] arguments) {
        System.out.print("Your weight on planet Earth is: ");
        double weight = 225;
        System.out.println(weight);
        
        System.out.print("Your weight on planet Mercury is: ");
        double mercury = weight * 0.378;
        System.out.println(mercury);
        
        System.out.print("Your weight on the moon is: ");
        double moon = weight * .166;
        System.out.println(moon);
        
        System.out.print("Your weight on Jupiter is: ");
        double jupiter = weight * 2.364;
        System.out.println(jupiter);
                
        

    }
}